#include<stdio.h>
int main(){
    int l;
    float q;
    scanf("%d",&l);
    if(l<=100000){
        q=l*0.1;
    }
    if(l>=100000&&l<=200000){
        q=100000*0.1+(l-100000)*0.075;
    }
    if(l>=200000&&l<=400000){
        q=100000*0.1+100000*0.075+(l-200000)*0.05;
    }
    if(l>=400000&&l<=600000){
        q=100000*0.1+100000*0.075+200000*0.05+(l-400000)*0.03;
    }
    if(l>=600000&&l<=1000000){
        q=100000*0.1+100000*0.075+200000*0.05+200000*0.03+(l-600000)*0.015;
    }
    if(l>=1000000){
        q=100000*0.1+100000*0.075+200000*0.05+200000*0.03+400000*0.015+(l-1000000)*0.01;
    }
    printf("%.2f",q);
    
    
}